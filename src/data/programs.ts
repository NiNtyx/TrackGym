function withStableIds(programs: Program[]): Program[] {
  return programs.map((p) => ({
    ...p,
    days: p.days.map((d) => ({
      ...d,
      exercises: d.exercises.map((e, i) => ({ ...e, id: `${d.id}-ex${i + 1}` })),
    })),
  }));
}

